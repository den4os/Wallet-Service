package io.ylab.walletservice.domain.entities;

/**
 * This enumeration represents the possible results of actions recorded in the audit log.
 * Each action can have one of two results: SUCCESS or FAIL.
 *
 * @author Denis Zanin
 * @version 1.0
 * @since 2023-10-10
 */
public enum ActionResult {
    SUCCESS,
    FAIL
}
